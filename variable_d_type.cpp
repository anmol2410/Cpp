#include<iostream>
using namespace std;

int main() {

    int a=12;
    cout<<"a = "<<a<<endl;

    char b='r';
    cout<<"b = "<<b<<endl;

    float f=5.0;
    cout<<"f = "<<f<<endl;

    double d=5.34;
    cout<<"d = "<<d<<endl;

    bool bl=true;
    cout<<"bl = "<<bl<<endl;

    cout<<"Size of bl = "<<sizeof(bl)<<endl;

    return 0;
}